def normalize_hostname(hostname):
    """將 localhost 轉換為 127.0.0.1，強制使用 TCP/IP 而不是 Unix socket"""
    # 在 CI 環境中，如果 hostname 為 None 或 localhost，都轉換為 127.0.0.1
    is_ci = os.getenv('GITHUB_ACTIONS') or os.getenv('CI')
    if is_ci:
        if not hostname or hostname == 'localhost':
            return '127.0.0.1'
    # 非 CI 環境中，只轉換 localhost
    if hostname == 'localhost':
        return '127.0.0.1'
    return hostname

def get_ssl_mode(hostname=None):
    """根據環境決定 SSL 模式"""
    # 判斷是否在 CI 環境
    is_ci = os.getenv('GITHUB_ACTIONS') or os.getenv('CI')
    # 判斷是否連接到 localhost
    is_localhost = hostname and hostname in ('localhost', '127.0.0.1', '::1')
    # 在 CI 環境或 localhost 連線時不使用 SSL，其他情況使用 SSL（適用於 Supabase 等雲端服務）
    return 'disable' if (is_ci or is_localhost) else 'require'

def parse_database_url(database_url):
    """解析 DATABASE_URL 連接字串"""
    try:
        # 如果是 SQLite URL，返回 None（讓它使用 SQLite 設定）
        if database_url.startswith('sqlite://') or database_url.startswith('sqlite3://'):
            return None
        
        # 處理 postgres:// 和 postgresql:// 兩種格式
        if database_url.startswith('postgres://'):
            database_url = database_url.replace('postgres://', 'postgresql://', 1)
        
        # 如果不是 PostgreSQL URL，返回 None
        if not database_url.startswith('postgresql://'):
            return None
        
        parsed = urlparse(database_url)
        
        # 將 localhost 轉換為 127.0.0.1，強制使用 TCP/IP 而不是 Unix socket
        # 這在 Docker 容器環境（如 GitHub Actions）中是必要的
        hostname = normalize_hostname(parsed.hostname)
        
        # 確保 HOST 不為空（空值會導致 psycopg2 使用 Unix socket）
        # 如果 hostname 仍然為空（不應該發生，但作為安全措施），在 CI 環境中使用 127.0.0.1
        if not hostname:
            is_ci = os.getenv('GITHUB_ACTIONS') or os.getenv('CI')
            if is_ci:
                hostname = '127.0.0.1'
        
        # 構建 OPTIONS，確保在 CI 環境中強制使用 TCP/IP
        # 增加連接超時時間，避免 Supabase 連接超時
        options = {
            'connect_timeout': 30,  # 增加到 30 秒，避免網路延遲導致的超時
            'sslmode': get_ssl_mode(hostname),  # CI 環境和 localhost 不使用 SSL，雲端服務使用 SSL
        }
        
        return {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': parsed.path[1:] if parsed.path.startswith('/') else parsed.path,  # 移除開頭的 /
            'USER': parsed.username,
            'PASSWORD': parsed.password,
            'HOST': hostname,  # 必須設置為非空值，否則會使用 Unix socket
            'PORT': parsed.port or '5432',
            'OPTIONS': options,
            'CONN_MAX_AGE': 600,  # 連接池：保持連接 10 分鐘，減少連接開銷
        }
    except Exception as e:
        # 如果解析失敗，返回 None
        return None

