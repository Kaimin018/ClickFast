# 過濾掉未登錄時的 401 警告（這是正常行為，不需要記錄為警告）
import logging

class SkipUnauthorizedFilter(logging.Filter):
    """過濾掉 /api/profile/ 的 401 未登錄警告"""
    def filter(self, record):
        # 檢查是否是 401 錯誤且路徑是 /api/profile/
        if hasattr(record, 'status_code') and record.status_code == 401:
            if hasattr(record, 'request') and hasattr(record.request, 'path'):
                if '/api/profile/' in record.request.path:
                    return False  # 過濾掉這個日誌
        return True  # 保留其他日誌

class SkipBrokenPipeFilter(logging.Filter):
    """過濾掉 Broken pipe 訊息（Selenium 測試中常見且無害）"""
    def filter(self, record):
        # 檢查是否是 Broken pipe 訊息
        if hasattr(record, 'msg') and isinstance(record.msg, str):
            if 'Broken pipe' in record.msg or 'ConnectionResetError' in str(record.getMessage()):
                return False  # 過濾掉這個日誌
        return True  # 保留其他日誌

class SafeFormatter(logging.Formatter):
    """安全的格式化器，處理缺少欄位的情況"""
    def format(self, record):
        # 如果缺少 method 或 path 欄位，使用簡單格式
        if not hasattr(record, 'method') or not hasattr(record, 'path'):
            # 使用簡單格式避免 KeyError
            simple_format = '[{asctime}] {levelname} {message}'
            formatter = logging.Formatter(simple_format, style='{')
            return formatter.format(record)
        # 否則使用預設格式
        return super().format(record)

